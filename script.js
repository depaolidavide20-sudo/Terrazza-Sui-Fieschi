const setupCarousel = ({
  carousel,
  slideSelector,
  dotSelector,
  nextSelector,
  prevSelector,
  dotDataKey,
  autoDelay
}) => {
  const slides = Array.from(carousel.querySelectorAll(slideSelector));
  const dots = Array.from(carousel.querySelectorAll(dotSelector));
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  let currentSlide = 0;
  let autoTimer;
  let startX = 0;
  let startY = 0;
  let suppressClick = false;
  let isCarouselVisible = false;
  let hasEnteredView = false;

  if (!slides.length) return;

  const setSlide = (index) => {
    currentSlide = (index + slides.length) % slides.length;
    slides.forEach((slide, slideIndex) => {
      slide.classList.toggle("is-active", slideIndex === currentSlide);
    });
    dots.forEach((dot, dotIndex) => {
      const isActive = dotIndex === currentSlide;
      dot.classList.toggle("is-active", isActive);
      if (isActive) {
        dot.setAttribute("aria-current", "true");
      } else {
        dot.removeAttribute("aria-current");
      }
    });
  };

  const stopAuto = () => window.clearInterval(autoTimer);
  const startAuto = () => {
    stopAuto();
    if (isCarouselVisible && !reducedMotion && slides.length > 1) {
      autoTimer = window.setInterval(() => setSlide(currentSlide + 1), autoDelay);
    }
  };
  const goToSlide = (index) => {
    setSlide(index);
    startAuto();
  };
  const controlSelector = `${nextSelector}, ${prevSelector}, ${dotSelector}`;
  const activateControl = (control) => {
    if (control.matches(nextSelector)) {
      goToSlide(currentSlide + 1);
    } else if (control.matches(prevSelector)) {
      goToSlide(currentSlide - 1);
    } else if (control.matches(dotSelector)) {
      goToSlide(Number(control.dataset[dotDataKey]));
    }
  };

  carousel.addEventListener("click", (event) => {
    const control = event.target.closest(controlSelector);
    if (!control) return;
    event.preventDefault();
    if (suppressClick) return;
    activateControl(control);
  });

  carousel.addEventListener("pointerdown", (event) => {
    const control = event.target.closest(controlSelector);
    if (control) {
      event.preventDefault();
      suppressClick = true;
      activateControl(control);
      window.setTimeout(() => {
        suppressClick = false;
      }, 350);
      return;
    }
    startX = event.clientX;
    startY = event.clientY;
    carousel.classList.add("is-dragging");
    stopAuto();
  });

  carousel.addEventListener("pointerup", (event) => {
    if (!carousel.classList.contains("is-dragging")) return;
    const deltaX = event.clientX - startX;
    const deltaY = event.clientY - startY;
    carousel.classList.remove("is-dragging");

    if (Math.abs(deltaX) > 46 && Math.abs(deltaX) > Math.abs(deltaY)) {
      goToSlide(deltaX < 0 ? currentSlide + 1 : currentSlide - 1);
    } else {
      startAuto();
    }
  });

  carousel.addEventListener("pointercancel", () => {
    carousel.classList.remove("is-dragging");
    startAuto();
  });

  carousel.addEventListener("mouseenter", stopAuto);
  carousel.addEventListener("focusin", stopAuto);
  carousel.addEventListener("mouseleave", startAuto);
  carousel.addEventListener("focusout", startAuto);

  setSlide(0);

  if ("IntersectionObserver" in window) {
    const carouselObserver = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        isCarouselVisible = entry.isIntersecting;
        if (isCarouselVisible) {
          if (!hasEnteredView) {
            setSlide(0);
            hasEnteredView = true;
          }
          startAuto();
        } else {
          stopAuto();
        }
      });
    }, { threshold: 0.35 });

    carouselObserver.observe(carousel);
  } else {
    isCarouselVisible = true;
    hasEnteredView = true;
    startAuto();
  }
};

