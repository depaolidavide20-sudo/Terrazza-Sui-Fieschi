const languageSwitcher = document.querySelector("[data-language-switcher]");
const languageButton = languageSwitcher?.querySelector(".language-button");
const languageOptions = languageSwitcher ? Array.from(languageSwitcher.querySelectorAll("[data-language]")) : [];
const translatableTextNodes = [];
const translatableAttributes = [];

const shouldSkipTranslation = (element) => {
  return !element || element.closest("script, style, noscript, .language-switcher");
};

const collectTranslatableText = () => {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  let node = walker.nextNode();

  while (node) {
    const key = normalizeText(node.textContent);
    if (key && !shouldSkipTranslation(node.parentElement)) {
      translatableTextNodes.push({ node, key });
    }
    node = walker.nextNode();
  }
};

const collectTranslatableAttributes = () => {
  const attributes = ["aria-label", "placeholder", "title", "alt"];
  document.querySelectorAll(attributes.map((attribute) => `[${attribute}]`).join(",")).forEach((element) => {
    if (shouldSkipTranslation(element)) return;

    attributes.forEach((attribute) => {
      const key = normalizeText(element.getAttribute(attribute) || "");
      if (key) translatableAttributes.push({ element, attribute, key });
    });
  });
};

collectTranslatableText();
collectTranslatableAttributes();

const applyLanguage = (language) => {
  const selectedLanguage = supportedLanguages.includes(language) ? language : "it";
  activeLanguage = selectedLanguage;
  document.documentElement.lang = selectedLanguage;
  storeLanguage(selectedLanguage);

  translatableTextNodes.forEach(({ node, key }) => {
    node.textContent = translateText(key, selectedLanguage);
  });

  translatableAttributes.forEach(({ element, attribute, key }) => {
    element.setAttribute(attribute, translateText(key, selectedLanguage));
  });

  document.title = translateText(baseSiteTitle, selectedLanguage);
  document.querySelector('meta[name="description"]')?.setAttribute("content", translateText(baseSiteDescription, selectedLanguage));
  languageButton.textContent = selectedLanguage.toUpperCase();
  languageOptions.forEach((option) => {
    const isActive = option.dataset.language === selectedLanguage;
    option.classList.toggle("is-active", isActive);
    option.setAttribute("aria-pressed", String(isActive));
  });
  mobileMenuToggle?.setAttribute(
    "aria-label",
    translateText(header?.classList.contains("is-menu-open") ? "Chiudi menu" : "Apri menu", selectedLanguage)
  );
};

